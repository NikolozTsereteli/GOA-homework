# 2)შექმენი ფუნქცია რომელსაც გადაეცემა რიცხვებით სავსე სია,თქვენი დავალებაა იპოვოთ მხოლოდ 50 ზე მეტი ტიცხვების საშუალო არითმეტიკული


def average(listt):
    sum = 0
    amount = 0
    for i in listt:
        if i > 50:
            sum += i
            amount += 1
    
    return sum / amount

print(average([1, 2, 3, 4454, 45, 56, 67, 78, 3, 4, 45, 35, 576, 67, 8, 87, 55, 5, 45,]))
