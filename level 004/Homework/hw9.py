# while ციკლი - გამოიყენება მაშინ, როდესაც არ ვიცით ზუსტად რამდენჯერ უნდა შესრულდეს ციკლი, ანუ ციკლი გაგრძელდება მანამ, სანამ გარკვეული პირობა სიმართლეა (True).

