# for ციკლი - გამოიყენება მაშინ როდესაც ვიცით თუ რამდენჯერ უნდა შესრულდეს ციკლი, ან როდესაც სიაში, - list, string, და ასე შემდეგ - გვინდა რომ გადავიაროთ ელემენტებზე.

